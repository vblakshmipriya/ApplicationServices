package com.comcast.technucleus.application.model;

public class TechBufferDevice
{
	private String SerialNumber;
	private String EquipmentTypeDesc;
	private String EquipmentTypeCd;
	private String EquipmentModelCd;
	private String EquipmentModelDesc;
	private String OwnerDesc;
	public String getSerialNumber() {
		return SerialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		SerialNumber = serialNumber;
	}
	public String getEquipmentTypeDesc() {
		return EquipmentTypeDesc;
	}
	public void setEquipmentTypeDesc(String equipmentTypeDesc) {
		EquipmentTypeDesc = equipmentTypeDesc;
	}
	public String getEquipmentTypeCd() {
		return EquipmentTypeCd;
	}
	public void setEquipmentTypeCd(String equipmentTypeCd) {
		EquipmentTypeCd = equipmentTypeCd;
	}
	public String getEquipmentModelCd() {
		return EquipmentModelCd;
	}
	public void setEquipmentModelCd(String equipmentModelCd) {
		EquipmentModelCd = equipmentModelCd;
	}
	public String getEquipmentModelDesc() {
		return EquipmentModelDesc;
	}
	public void setEquipmentModelDesc(String equipmentModelDesc) {
		EquipmentModelDesc = equipmentModelDesc;
	}
	public String getOwnerDesc() {
		return OwnerDesc;
	}
	public void setOwnerDesc(String ownerDesc) {
		OwnerDesc = ownerDesc;
	}
	
}
